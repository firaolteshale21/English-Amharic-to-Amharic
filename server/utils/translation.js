const translateText = (text) => {
  let translatedText = "";
  let i = 0;

  while (i < text.length) {
    // Try to match the longest substring first (up to 4 characters)
    if (i + 3 < text.length && englishToAmharic[text.slice(i, i + 4)]) {
      translatedText += englishToAmharic[text.slice(i, i + 4)];
      i += 4;
    } else if (i + 2 < text.length && englishToAmharic[text.slice(i, i + 3)]) {
      translatedText += englishToAmharic[text.slice(i, i + 3)];
      i += 3;
    } else if (i + 1 < text.length && englishToAmharic[text.slice(i, i + 2)]) {
      translatedText += englishToAmharic[text.slice(i, i + 2)];
      i += 2;
    } else if (englishToAmharic[text[i]]) {
      translatedText += englishToAmharic[text[i]];
      i += 1;
    } else {
      translatedText += text[i];
      i += 1;
    }
  }

  return translatedText;
};

